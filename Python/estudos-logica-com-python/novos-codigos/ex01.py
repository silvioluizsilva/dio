# Neste algoritmo, crie uma variável que armazene uma string e uma lista que armazena várias strings.

mensagem = "Olá, mundo!"

lista_mensagens = ["Olá, mundo!", "Bem-vindo ao Python!", "Aprendendo programação.", "Divirta-se codificando!"]

print("Mensagem única:", mensagem)
print("Lista de mensagens:")
for msg in lista_mensagens:
    print("-", msg)